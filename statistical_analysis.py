# Table 4: MIA validation — empirical vs. theoretical bound
# ---------------------------------------------------------------------------

def table_mia_validation(results_dir):
    """
    Empirical MIA accuracy vs. Yeom et al. theoretical bound at each ε.

    Columns: epsilon | mechanism | empirical_acc | theo_bound | margin | auc | status
    """
    path = os.path.join(results_dir, 'mia_results.csv')
    if not os.path.exists(path):
        print('  mia_results.csv not found.')
        return []

    df = pd.read_csv(path)
    rows = []

    for _, r in df.iterrows():
        emp = float(r.get('empirical_accuracy', 0.5))
        theo = float(r.get('theoretical_max_accuracy', 1.0))
        margin = theo - emp
        status = 'VALID' if emp <= theo + 1e-4 else 'VIOLATED'

        rows.append({
            'epsilon': r.get('epsilon', ''),
            'mechanism': r.get('mechanism', ''),
            'feature': r.get('feature', ''),
            'empirical_accuracy': round(emp, 4),
            'theoretical_bound': round(theo, 4),
            'margin_pp': round(margin * 100, 2),
            'empirical_advantage': round(float(r.get('empirical_advantage', 0)), 4),
            'theoretical_max_advantage': round(float(r.get('theoretical_max_advantage', 0)), 4),
            'auc': round(float(r.get('auc', 0.5)), 4),
            'dp_guarantee_status': status,
        })

    _save(rows, os.path.join(results_dir, 'stat_table4_mia.csv'))
    return rows


