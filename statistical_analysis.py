# Table 7: Multi-class IDS — per-class accuracy at each ε
# ---------------------------------------------------------------------------

def table_multiclass_summary(results_dir):
    """Per-class accuracy for the 5-category IDS at each ε."""
    path = os.path.join(results_dir, 'multiclass_results.csv')
    if not os.path.exists(path):
        print('  multiclass_results.csv not found.')
        return []

    df = pd.read_csv(path)
    rows = []
    class_names = ['Normal', 'DoS', 'Probe', 'R2L', 'U2R']

    for _, r in df.iterrows():
        row = {
            'epsilon': r.get('epsilon', ''),
            'budget_method': r.get('budget_method', ''),
            'overall_accuracy': round(float(r.get('accuracy', 0)), 4),
            'macro_f1': round(float(r.get('macro_f1', 0)), 4),
            'acc_loss_pct': round(float(r.get('relative_accuracy_loss_pct', 0)), 2),
        }
        for cn in class_names:
            col = f'acc_{cn}'
            if col in r:
                row[col] = round(float(r[col]), 4) if not pd.isna(r[col]) else 'N/A'
        rows.append(row)

    _save(rows, os.path.join(results_dir, 'stat_table7_multiclass.csv'))
    return rows


# ---------------------------------------------------------------------------
# Master summary — one table to rule them all
# ---------------------------------------------------------------------------

def generate_master_summary(results_dir):
    """Generate a concise 1-page summary table with the most important numbers."""
    lines = []
    lines.append('=' * 70)
    lines.append('DIFFERENTIAL PRIVACY FOR IDS — KEY RESULTS SUMMARY')
    lines.append('Dataset: NSL-KDD (125,973 records, 37 features)')
    lines.append('=' * 70)

    # DP-ML results
    ml_path = os.path.join(results_dir, 'dp_ml_results.csv')
    sgd_path = os.path.join(results_dir, 'dpsgd_results.csv')
    if os.path.exists(ml_path) and os.path.exists(sgd_path):
        df_ml = pd.read_csv(ml_path)
        df_sgd = pd.read_csv(sgd_path)
        baseline = df_ml[df_ml['mechanism'] == 'none']['accuracy'].values
        base_acc = float(np.mean(baseline)) if len(baseline) > 0 else 0.0

        lines.append(f'\nBinary Classification (Normal vs. Attack):')
        lines.append(f'  Baseline (no DP): {base_acc:.4f} accuracy')
        lines.append(f'  {"ε":>6}  {"Input-Uniform":>16}  {"Input-MI":>14}  {"DP-SGD":>12}')

        epsilons_ml = sorted(df_ml[df_ml['mechanism'] != 'none']['epsilon'].unique())
        for eps in epsilons_ml:
            u = df_ml[(df_ml['epsilon'] == eps) & (df_ml['budget_method'] == 'uniform')]['accuracy'].values
            m = df_ml[(df_ml['epsilon'] == eps) & (df_ml['budget_method'] == 'mi')]['accuracy'].values
            s = df_sgd[df_sgd['epsilon'] == eps]['accuracy'].values
            u_s = f'{np.mean(u):.4f}±{1.96*np.std(u)/np.sqrt(len(u)):.4f}' if len(u) > 0 else 'N/A'
            m_s = f'{np.mean(m):.4f}±{1.96*np.std(m)/np.sqrt(len(m)):.4f}' if len(m) > 0 else 'N/A'
            s_s = f'{np.mean(s):.4f}±{1.96*np.std(s)/np.sqrt(len(s)):.4f}' if len(s) > 0 else 'N/A'
            lines.append(f'  {eps:>6.1f}  {u_s:>16}  {m_s:>14}  {s_s:>12}')

    # Clipped sensitivity
    cs_path = os.path.join(results_dir, 'clipped_sensitivity.csv')
    if os.path.exists(cs_path):
        df_cs = pd.read_csv(cs_path)
        sub99 = df_cs[df_cs['clip_percentile'] == 99.0]['noise_reduction']
        sub99 = sub99[np.isfinite(sub99) & (sub99 > 0)]
        lines.append(f'\nClipped Sensitivity (99th percentile):')
        lines.append(f'  Median noise reduction: {np.median(sub99):.1f}×')
        lines.append(f'  Max noise reduction: {np.max(sub99):.0f}× (su_attempted)')
        lines.append(f'  Features with >1000× reduction: {int(np.sum(sub99 > 1000))}/37')

    # RDP composition
    rdp_path = os.path.join(results_dir, 'rdp_composition.csv')
    if os.path.exists(rdp_path):
        df_rdp = pd.read_csv(rdp_path)
        k100 = df_rdp[df_rdp['n_queries'] == 100]
        if len(k100) > 0:
            r = k100.iloc[0]
            rdp = float(r.get('rdp_composition_epsilon', r.get('rdp_epsilon', 0)))
            basic = float(r.get('basic_composition_epsilon', r.get('basic_epsilon', 0)))
            ratio = basic / rdp if rdp > 0 else 1.0
            lines.append(f'\nComposition (k=100, ε₀={r.get("nominal_epsilon", "?")}, Laplace):')
            lines.append(f'  Basic: {basic:.3f}, RDP: {rdp:.3f}  ({ratio:.2f}× tighter)')

    # MIA validation
    mia_path = os.path.join(results_dir, 'mia_results.csv')
    if os.path.exists(mia_path):
        df_mia = pd.read_csv(mia_path)
        violated = df_mia[df_mia['empirical_accuracy'] > df_mia['theoretical_max_accuracy'] + 0.001]
        lines.append(f'\nMembership Inference Attacks:')
        lines.append(f'  {len(df_mia)} attack configurations tested')
        lines.append(f'  DP bound violations: {len(violated)}/{ len(df_mia)} (0 = VALID guarantee)')

    lines.append('\n' + '=' * 70)
    summary_text = '\n'.join(lines)
    out_path = os.path.join(results_dir, 'master_summary.txt')
    with open(out_path, 'w') as f:
        f.write(summary_text)
    print(f'  Saved {out_path}')
    print(summary_text)
    return summary_text


# ---------------------------------------------------------------------------
# Run all tables
# ---------------------------------------------------------------------------

def run_all_tables(results_dir='results'):
    print('=== Statistical Analysis ===')
    table_dpml_summary(results_dir)
    table_composition_tightness(results_dir)
    table_clipped_sensitivity(results_dir)
    table_mia_validation(results_dir)
    table_ldp_penalty(results_dir)
    table_budget_significance(results_dir)
    table_multiclass_summary(results_dir)
    generate_master_summary(results_dir)
    print('=== Done ===')


if __name__ == '__main__':
    import argparse
    p = argparse.ArgumentParser()
    p.add_argument('--results_dir', default='results')
    args = p.parse_args()
    run_all_tables(args.results_dir)
