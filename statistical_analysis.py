# Table 5: LDP vs. Central DP — MSE penalty
# ---------------------------------------------------------------------------

def table_ldp_penalty(results_dir):
    """
    MSE ratio (LDP error / central DP error) across mechanisms and ε values.

    This quantifies the cost of the local privacy model (no trusted curator).
    """
    ldp_path = os.path.join(results_dir, 'ldp_results.csv')
    central_path = os.path.join(results_dir, 'central_dp_results.csv')
    if not os.path.exists(ldp_path) or not os.path.exists(central_path):
        print('  LDP results not found.')
        return []

    ldp = pd.read_csv(ldp_path)
    central = pd.read_csv(central_path)
    rows = []

    features = ldp['feature'].unique() if 'feature' in ldp.columns else []
    for feature in features:
        ldp_f = ldp[ldp['feature'] == feature]
        central_f = central[central['feature'] == feature] if 'feature' in central.columns else central

        for eps in sorted(ldp_f['epsilon'].unique()):
            central_lap = central_f[
                (central_f['epsilon'] == eps) & (central_f['mechanism'] == 'laplace')
            ]['mean_error'].values

            if len(central_lap) == 0:
                continue
            c_err = float(central_lap[0])

            for mech in ldp_f['mechanism'].unique():
                ldp_err_vals = ldp_f[
                    (ldp_f['epsilon'] == eps) & (ldp_f['mechanism'] == mech)
                ]['mean_error'].values
                if len(ldp_err_vals) == 0:
                    continue
                l_err = float(ldp_err_vals[0])

                rows.append({
                    'feature': feature,
                    'epsilon': float(eps),
                    'ldp_mechanism': mech,
                    'ldp_error': round(l_err, 6),
                    'central_laplace_error': round(c_err, 6),
                    'mse_penalty_ratio': round(l_err / c_err, 2) if c_err > 1e-12 else float('inf'),
                    'penalty_db': round(10 * np.log10(l_err / c_err), 2) if c_err > 1e-12 and l_err > 0 else float('inf'),
                })

    _save(rows, os.path.join(results_dir, 'stat_table5_ldp_penalty.csv'))
    return rows


# ---------------------------------------------------------------------------
# Table 6: Budget allocation comparison — per-epsilon significance
# ---------------------------------------------------------------------------

def table_budget_significance(results_dir):
    """
    Wilcoxon p-values for MI-weighted vs. uniform budget at each ε.
    Also includes variance-weighted and SNR-weighted comparisons.
    """
    path = os.path.join(results_dir, 'budget_comparison.csv')
    if not os.path.exists(path):
        print('  budget_comparison.csv not found.')
        return []

    df = pd.read_csv(path)
    rows = []

    epsilons = sorted(df[df['mechanism'] != 'none']['epsilon'].unique())
    methods = [m for m in df['budget_method'].unique() if m != 'uniform']

    for eps in epsilons:
        acc_uniform = df[
            (df['epsilon'] == eps) & (df['budget_method'] == 'uniform')
        ]['accuracy'].values

        if len(acc_uniform) == 0:
            continue

        row = {
            'epsilon': float(eps),
            'uniform_accuracy': round(float(np.mean(acc_uniform)), 4),
        }
        for m in methods:
            acc_m = df[
                (df['epsilon'] == eps) & (df['budget_method'] == m)
            ]['accuracy'].values
            if len(acc_m) == 0:
                continue
            row[f'{m}_accuracy'] = round(float(np.mean(acc_m)), 4)
            row[f'{m}_vs_uniform_p'] = round(_wilcoxon(acc_m, acc_uniform), 4)
            row[f'{m}_delta_accuracy_pp'] = round((float(np.mean(acc_m)) - float(np.mean(acc_uniform))) * 100, 3)

        rows.append(row)

    _save(rows, os.path.join(results_dir, 'stat_table6_budget_significance.csv'))
    return rows


